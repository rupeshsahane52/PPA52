class display1to10
{
    ////////////////////////////////////////////////////////////////////////////////
    // Fuction name : Display the no 
    // Input: Integer
    // Output : Integer
    //Author : Rupesh Ravindra Sahane
    // Date : 13/08/23
    ////////////////////////////////////////////////////////////////////////////////


    ///////////////////////////////////////////////////////////////////////////////
    //Entey point of appplication
    ///////////////////////////////////////////////////////////////////////////////

    public static void main(String arg[])
    {
        int x = 1;

        do{
            System.out.println(x);
            x++;
        }while(x <= 10);

    }
}

