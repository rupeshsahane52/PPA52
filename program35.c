int main()
{
    float a,b,c, s,area;

    printf("Enter the sides of triangle1 : \t");
    scanf("%f",&a);

     printf("Enter the sides of triangle2 : \t");
    scanf("%f",&b);

     printf("Enter the sides of triangle3 : \t");
    scanf("%f",&c);

    s = (a+b+c)/2;

    area = sqrt(s*(s-a)*(s-b)*(s-c));

    printf("The Area of Triangle %6.2f sq units\n",area);
    

    return 0;

}

