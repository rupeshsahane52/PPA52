
// Find the even and odd no

#include<stdio.h>

int main()
{

    int a = 12;

    if(a % 2 == 0)
    {
        printf("No is even\n");
    }
    else
    {
        printf("No is odd\n");
    }
    return 0;
}
//////////////////////////////////////////////////////////////////////////////////////
// Result : No is even
